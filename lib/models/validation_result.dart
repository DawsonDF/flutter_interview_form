/// Result of a field validation
class ValidationResult {
  final bool isValid;
  final String? errorMessage;
  final bool isValidating;
  
  const ValidationResult({
    required this.isValid,
    this.errorMessage,
    this.isValidating = false,
  });
  
  /// Successful validation
  const ValidationResult.valid() : this(isValid: true);
  
  /// Failed validation with error message
  const ValidationResult.invalid(String errorMessage) 
      : this(isValid: false, errorMessage: errorMessage);
  
  /// Currently validating (async operation in progress)
  const ValidationResult.validating() 
      : this(isValid: false, isValidating: true);
}

